// Search function
export const searchMedicines = (query) => {
  const searchTerm = query.toLowerCase().trim()

  if (!searchTerm) return []

  return medicines.filter(
    (medicine) =>
      medicine.name.toLowerCase().includes(searchTerm) ||
      medicine.brand.toLowerCase().includes(searchTerm) ||
      medicine.category.toLowerCase().includes(searchTerm) ||
      medicine.uses.some((use) => use.toLowerCase().includes(searchTerm)) ||
      medicine.description.toLowerCase().includes(searchTerm),
  )
}

// Get medicines by category
export const getMedicinesByCategory = (category) => {
  return medicines.filter((medicine) => medicine.category === category)
}

// Get medicine by ID
export const getMedicineById = (id) => {
  return medicines.find((medicine) => medicine.id === Number.parseInt(id))
}

// Get trending medicines
export const getTrendingMedicines = () => {
  return medicines
    .filter((medicine) => medicine.rating >= 4.0)
    .sort((a, b) => b.rating - a.rating)
    .slice(0, 8)
}

